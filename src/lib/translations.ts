export function translateShelfLife(shelf: string | null, lang: string): string | null {
  if (!shelf) return null;
  const t = getTranslations(lang);
  
  // Format: "X Months" or "X Year" / "X Years"
  const matchMonths = shelf.match(/^(\d+)\s+Months?$/i);
  if (matchMonths) {
    const num = matchMonths[1];
    return `${num} ${t.unit_months}`;
  }
  
  const matchYears = shelf.match(/^(\d+)\s+Years?$/i);
  if (matchYears) {
    const num = matchYears[1];
    return `${num} ${t.unit_years}`;
  }
  
  const matchYear = shelf.match(/^(\d+)\s+Year$/i);
  if (matchYear) {
    const num = matchYear[1];
    return `${num} ${t.unit_year}`;
  }

  return shelf;
}
